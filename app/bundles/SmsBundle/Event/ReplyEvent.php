namespace Mautic\SmsBundle\Event;

class ReplyEvent extends AbstractCallbackEvent
{
    /**
     * @var string
     */
    private $message;

    /**
     * @return string
     */
    public function getMessage()
    {
        return $this->message;
    }

    /**
     * @param string $message
     *
     * @return $this
     */
    public function setMessage($message)
    {
        $this->message = $message;
    }
}
